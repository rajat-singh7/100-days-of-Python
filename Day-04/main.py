#Basic time greeting mini-project:
time = int(input("Enter hour in 24 hour format, ex-0-23:"))
if(time >= 4 and time < 12):
    print("Good Morning 🌄")
elif(time >= 12 and time < 17):
    print("Good Afternoon 🌅")
elif(time >= 17 and time < 20):
    print("Good evening 🌆")
else:
    print("good night 🌃")


